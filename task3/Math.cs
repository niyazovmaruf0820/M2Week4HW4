namespace task3;

public static class Math
{
    public static int Add(int a, int b)
    {
        return a + b;
    }
    public static int Substract(int a, int b)
    {
        return a - b;
    }
    public static int Multiply(int a, int b)
    {
        return a * b;
    }
    public static int Divide(int a, int b)
    {
        return a / b;
    }
}
